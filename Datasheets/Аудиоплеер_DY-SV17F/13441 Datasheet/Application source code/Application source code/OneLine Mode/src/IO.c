#include "IO.h"

//--------------------------
void IO_Init(void)
{
  //oneline->PD4配置
  GPIOD->DDR |= (1<<4);    //PD4 output
  GPIOD->CR1 |= (1<<4);
  GPIOD->ODR |= (1<<4);
}
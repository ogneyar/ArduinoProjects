#include "led.h"

//-------------定义LED的管脚的模式-------------
void LED_Init(void)
{
  GPIOB->DDR |= (1<<5);
  GPIOB->CR1 |= (1<<5);
  GPIOB->CR2 |= (1<<5);
}
//-------------LED关闭-------------
void LED_OFF(void)
{
  GPIOB->ODR |= (1<<5);
}
//-------------LED打开-------------
void LED_ON(void)
{
  
  GPIOB->ODR &= ~(1<<5);
}
